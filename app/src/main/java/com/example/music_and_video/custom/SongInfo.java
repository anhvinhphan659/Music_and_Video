package com.example.music_and_video.custom;

public class SongInfo {
    public String id;
    public String name;
    public String singer;
    public String path;
    public SongInfo(String id, String name, String singer, String path) {
        this.id = id;
        this.name = name;
        this.singer = singer;
        this.path = path;
    }
}
