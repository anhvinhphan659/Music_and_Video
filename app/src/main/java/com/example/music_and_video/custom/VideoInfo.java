package com.example.music_and_video.custom;

public class VideoInfo {
    public String id;
    public String name;
    public String path;
    public int size;
    public int length;

    public VideoInfo(String id, String name, String path, int size, int length) {
        this.id = id;
        this.name = name;
        this.path = path;
        this.size = size;
        this.length = length;
    }
}
